kate = (shadoofs //  nums) * 2                          # находим количество сделанное Катей
petya = (shadoofs - kate) // 2                          # находим остаток: путем ВЫЧЕТАНИЯ количества журавлей Кати от общего числа и делим остаток между мальчиками
sergey = petya
print(f'Петя сделал: {petya} журавликов , Катя сделала: {kate} журавликов, Сергей сделал: {sergey} журавликов')

